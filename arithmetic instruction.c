#include<stdio.h>
#include<math.h>
int main()
{
    int a=4,b=10;

    int z=b*a;
    printf("value of  z is %d\n",z);

    printf("the value of a+b is %d\n",a+b);
    printf("the value of a-b is %d\n",a-b);
    printf("the value of a*b is %d\n",a*b);
    printf("the value of a/b is %d\n",a/b);


    printf("value of a%b is %d\n",8%3);
    printf("value of 4 to the power 6 is %.f\n",pow(4,6));

    return 0;
}
