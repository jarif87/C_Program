#include<stdio.h>
int main()
{
    /*relational_operator===>>>>

    <, <= ,> ,>=, ==, != 
    
    */
    int number_1=10,number_2=30;


    printf("First Result is %d\n",number_1<number_2); //less than
    
    printf("Second Result is %d\n",number_1<=number_2);
    
    printf("third  Result is %d\n",number_1>number_2);//greater than
    
    printf("Fourth Result is %d\n",number_1>=number_2);
    
    printf("Fifth  Result is %d\n",number_1==number_2); //== equal to
    printf("Sixth Result is %d\n",number_1!=number_2); //not equal



    return 0;
}