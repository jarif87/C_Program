#include<stdio.h>
int main()
{
    int num;


    while(1)
    {

printf("Enter your number:\n");
scanf("%d",&num);

switch(num)
{
case 0:
printf("this is case 0\n");
break;
case 1:
printf("this is case 1\n");
break;
case 2:
printf("this is case 2\n");
break;
case 3:
printf("this is case 3\n");
break;
case 4:
printf("this is case 4\n");
break;
case 5:
printf("this is case 5\n");
break;

default:
printf("this is default\n");
break;


    }



}



    return 0;
}